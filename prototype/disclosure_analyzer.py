# 2. 분류기
# =============================================================================
@dataclass
class DisclosureResult:
    code: str
    name: str
    category: str
    risk_score: int
    risk_label: str
    risk_color: str
    matched_keywords: List[str]
    confidence: float  # 0~1
    explanation: str
    checkpoints: List[str]
    similar_cases: List[str] = field(default_factory=list)


def classify(text: str) -> List[DisclosureResult]:
    """공시 본문/제목 텍스트를 받아 매칭되는 유형들을 위험도 순으로 반환."""
    if not text or not isinstance(text, str):
        return []
    matches = []
    for code, name, category, risk, patterns in DISCLOSURE_TYPES:
        matched = []
        for p in patterns:
            try:
                if re.search(p, text):
                    matched.append(p)
            except re.error:
                if p in text:
                    matched.append(p)
        if matched:
            # 신뢰도 = 매칭된 키워드 수 / 패턴 수, 텍스트 길이 보정
            conf = min(1.0, len(matched) / len(patterns) + 0.3)
            explanation = generate_explanation(code, name, category, risk, matched, text)
            checkpoints = generate_checkpoints(code, risk)
            similar = generate_similar_cases(code)
            matches.append(DisclosureResult(
                code=code, name=name, category=category,
                risk_score=risk, risk_label=risk_label(risk),
                risk_color=risk_color(risk),
                matched_keywords=matched, confidence=conf,
                explanation=explanation, checkpoints=checkpoints,
                similar_cases=similar,
            ))
    # 위험도 절댓값 큰 것부터
    matches.sort(key=lambda r: (-abs(r.risk_score), -r.confidence))
    return matches


