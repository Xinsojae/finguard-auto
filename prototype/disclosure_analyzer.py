# 위험도 → 5단계 라벨
def risk_label(score: int) -> str:
    if score <= -3: return "매우 높음"
    if score == -2: return "높음"
    if score == -1: return "중간"
    if score == 0:  return "정보"
    if score == 1:  return "낮음 (긍정)"
    if score == 2:  return "낮음 (긍정 ↑)"
    return "낮음 (매우 긍정)"

def risk_color(score: int) -> str:
    if score <= -2: return "#C62828"  # red
    if score == -1: return "#F57C00"  # orange
    if score == 0:  return "#9E9E9E"  # gray
    return "#2E7D32"                   # green


